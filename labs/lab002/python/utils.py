def key_expansion_EIC(key: list[int], Nk: int, Nr: int) -> list[list[int]]:
    i = 0
    total_words = 4 * (Nr + 1)

    w = [None] * total_words
    dw = [None] * total_words

    while i < Nk:
        w[i] = key[4 * i : 4 * i + 3+1]
        dw[i] = w[i][:]
        i += 1

    while i < total_words:
        tmp = w[i - 1][:] #copy

        if i % Nk == 0:
            tmp = sub_word(rot_word(tmp))
            r_con = Rcon[i // Nk - 1]
            tmp = xor_words(tmp, r_con)

        elif Nk > 6 and i % Nk == 4:
            tmp = sub_word(tmp)

        w[i] = xor_words(w[i - Nk], tmp)
        dw[i] = w[i][:]
        i += 1

    for round in range(1, Nr):
        i = 4 * round
        dw[i:i+3 + 1] = inv_mix_columns(dw[i:i+3 + 1])

    return dw




