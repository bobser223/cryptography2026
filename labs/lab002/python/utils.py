def inv_shift_rows(state: list[list[int]]) -> list[list[int]]:
    for i in range(1, 4):
        state[i] = cycle_shift(state[i], -i)
    return state

def inv_s_box(byte: int) -> int:
    return INV_SBOX[byte >> 4][byte & 0x0F]

def inv_sub_bytes(state: list[list[int]]) -> list[list[int]]:
    for i in range(4):
        for j in range(4):
            state[i][j] = inv_s_box(state[i][j])
    return state

def inv_mix_columns(state: list[list[int]]) -> list[list[int]]:
    for col in range(4):
        s_0 = state[0][col]
        s_1 = state[1][col]
        s_2 = state[2][col]
        s_3 = state[3][col]

        state[0][col] = (
            mul_in_gf256(14, s_0) ^
            mul_in_gf256(11, s_1) ^
            mul_in_gf256(13, s_2) ^
            mul_in_gf256(9,  s_3)
        )

        state[1][col] = (
            mul_in_gf256(9,  s_0) ^
            mul_in_gf256(14, s_1) ^
            mul_in_gf256(11, s_2) ^
            mul_in_gf256(13, s_3)
        )

        state[2][col] = (
            mul_in_gf256(13, s_0) ^
            mul_in_gf256(9,  s_1) ^
            mul_in_gf256(14, s_2) ^
            mul_in_gf256(11, s_3)
        )

        state[3][col] = (
            mul_in_gf256(11, s_0) ^
            mul_in_gf256(13, s_1) ^
            mul_in_gf256(9,  s_2) ^
            mul_in_gf256(14, s_3)
        )

    return state

def inv_cipher(inp, Nr, w):
    state = [row[:] for row in inp] #copy

    state = add_round_key(state, w[4 * Nr : 4 * Nr+3 + 1])

    for round_idx in range(Nr - 1, 0, -1):
        state = inv_shift_rows(state)
        state = inv_sub_bytes(state)
        state = add_round_key(state, w[4 * round_idx : 4 * round_idx + 4])
        state = inv_mix_columns(state)

    state = inv_shift_rows(state)
    state = inv_sub_bytes(state)
    state = add_round_key(state, w[0:4])

    return state

