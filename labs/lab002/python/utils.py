def xtime(x: int) -> int:
    x <<= 1
    if x & 0x100:
        x ^= 0x11B
    return x & 0xFF

def mul_in_gf256(gf_element: int, x: int) -> int:
    if gf_element == 1:
        return x
    if gf_element == 2:
        return xtime(x)
    if gf_element == 3:
        return xtime(x) ^ x
    if gf_element == 9:
        return xtime(xtime(xtime(x))) ^ x
    if gf_element == 11:
        return xtime(xtime(xtime(x))) ^ xtime(x) ^ x
    if gf_element == 13:
        return xtime(xtime(xtime(x))) ^ xtime(xtime(x)) ^ x
    if gf_element == 14:
        return xtime(xtime(xtime(x))) ^ xtime(xtime(x)) ^ xtime(x)

    raise ValueError("Unsupported multiplier")

def cycle_shift(lst: list[int], shift: int) -> list[int]:
    return lst[shift:] + lst[:shift]

def s_box(byte: int) -> int:
    row = byte >> 4        # старші 4 біти
    col = byte & 0x0F      # молодші 4 біти
    return SBOX[row][col]

def add_round_key(state: list[list[int]], round_key_words: list[list[int]]) -> list[list[int]]:
    for c in range(4):
        for r in range(4):
            state[r][c] ^= round_key_words[c][r]
    return state

def sub_bytes(state: list[list[int]]):
    for i in range(4):
        for j in range(4):
            state[i][j] = s_box(state[i][j])
    return state

def shift_rows(state: list[list[int]]) -> list[list[int]]:
    for i in range(1, 4):
        state[i] = cycle_shift(state[i], i)
    return state

def mix_columns(state: list[list[int]]) -> list[list[int]]:
    for col in range(4):
        s_0 = state[0][col]
        s_1 = state[1][col]
        s_2 = state[2][col]
        s_3 = state[3][col]
        state[0][col] = mul_in_gf256(2, s_0) ^ mul_in_gf256(3, s_1) ^ s_2 ^ s_3
        state[1][col] = s_0 ^ mul_in_gf256(2, s_1) ^ mul_in_gf256(3, s_2) ^ s_3
        state[2][col] = s_0 ^ s_1 ^ mul_in_gf256(2, s_2) ^ mul_in_gf256(3, s_3)
        state[3][col] = mul_in_gf256(3, s_0) ^ s_1 ^ s_2 ^ mul_in_gf256(2, s_3)
    return state

def cifer(inp, Nr, w):
    state = [row[:] for row in inp]
    state = add_round_key(state, w[:4])

    for round in range(1, Nr):
        state = sub_bytes(state)
        state = shift_rows(state)
        state = mix_columns(state)
        state = add_round_key(state, w[round * 4: round * 4+3 + 1])

    state = sub_bytes(state)
    state = shift_rows(state)
    state = add_round_key(state, w[Nr * 4: Nr * 4+3 + 1])

    return state

