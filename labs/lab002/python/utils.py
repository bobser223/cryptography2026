def sub_word(word: list[int]) -> list[int]:
    return [s_box(word[0]), s_box(word[1]), s_box(word[2]), s_box(word[3])]

def rot_word(word: list[int]) -> list[int]:
    return [word[1], word[2], word[3], word[0]]

def xor_words(a: list[int], b: list[int]) -> list[int]:
    return [x ^ y for x, y in zip(a, b)]

def key_expansion(key, Nk: int, Nr: int) -> list[list[int]]:
    i = 0
    total_words = 4 * (Nr + 1)

    w = [None] * total_words

    while i < Nk:
        w[i] = key[4 * i : 4 * i + 3+1]
        i += 1

    while i < total_words:
        tmp = w[i - 1][:]  # copy

        if i % Nk == 0:
            tmp = sub_word(rot_word(tmp))
            r_con = Rcon[i // Nk - 1]
            tmp = xor_words(tmp, r_con)

        elif Nk > 6 and i % Nk == 4:
            tmp = sub_word(tmp)

        w[i] = xor_words(w[i - Nk], tmp)
        i += 1
    return w




